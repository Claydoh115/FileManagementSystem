using System;

public class User
{
    public int uID;
    public String userName;
    public String email;
    public String fName;
    public String lName;
    public String accountType;


	public User(int uID, String userName, String email, String fName, String lName, String accountType)
	{
        this.uID = uID;
        this.userName = userName;
        this.email = email;
        this.fName = fName;
        this.lName = lName;
        this.accountType = accountType;
	}












}
